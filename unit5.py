class Dog:
    def __init__(self,name,colour,age):
        self.name = name
        self.colour = colour
        self.age = age

Dawa = Dog("Dawa", "Blue", 24)
Nima = Dog("Nima", "Pink", 34)
Blacky = Dog("Blacky", "White", 44)

print(Dawa.name)
print(Dawa.colour)
print(Dawa.age)
print(Nima.name)
print(Nima.colour)
print(Nima.age)
